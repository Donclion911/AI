target=class{
    constructor(x,y,size){
        this.x=x;
        this.y=y;
        this.size=size;
    }
    show(){
        x.beginPath();
        x.arc(this.x,this.y,this.size,0,2*Math.PI);
        x.strokeStyle="red";
        x.stroke();
        x.closePath();
    }
}
hit=new target(temp.width/2,40,10);
shot=class{
    constructor(speed,angle,path)
    {
        this.x=temp.width/2;
        this.y=temp.height-20;
        this.speed=speed;
        this.angle=angle;
        this.path=path;
        this.touched=false;
    }
    show()
    {
        x.beginPath();
        x.save()
        x.rect(this.x,this.y,5,20);
        x.restore();
        x.strokeStyle="white";
        x.stroke();
        x.closePath();
        if(!this.touched)
        {
            this.y-=this.speed*Math.sin(this.angle);
            this.x-=this.speed*Math.cos(this.angle);
            if(this.y<=0||this.x<=0||this.x>temp.width)
            {
                this.y=temp.height-20;
                this.x=temp.width/2;
            }
            if(this.y<=hit.y&&this.y>=hit.y-20 && this.x>=hit.x-15 &&this.x<=hit.x+15)
            {
                console.log("hit");
                this.touched=true;
            }
        }
    }
}
